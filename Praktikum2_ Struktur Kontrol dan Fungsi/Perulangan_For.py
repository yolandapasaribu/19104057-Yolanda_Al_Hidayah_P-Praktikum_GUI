#Menggunakan variabel
angka = 10

#Paramater cuma 1 : max
print ('#Paramater cuma 1 : max')
for i in range(angka) :
    print("Angka ke : ")
    print(i)

#Paramater cuma 2 : min, max ( + incre , - decre)
print ('#Paramater cuma 2 : min, max')
for i in range(angka,20) :
    print("Angka ke : ")
    print(i)

print ('#Paramater cuma 3 : min,max, lompat')
#Paramater cuma 3 : nilai awal, nilai akhir, lompat
for i in range(angka, 3,-2) :
    print("Angka ke : ")
    print(i)


#Menggunakann array
array = [1,2,3,4]

for i in array :
    print(i,end = ' ')







